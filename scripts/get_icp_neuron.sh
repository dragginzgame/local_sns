#!/bin/bash
# Script to fetch ICP neuron information
# This queries the ICP Governance canister for neuron details
#
# Usage:
#   bash scripts/get_icp_neuron.sh [neuron_id]
#
# Arguments:
#   neuron_id - Optional: Specific neuron ID to query. If not provided, uses neuron from deployment data
#
# Example:
#   bash scripts/get_icp_neuron.sh
#   bash scripts/get_icp_neuron.sh 1281960829742175837

set -euo pipefail

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Helper functions
print_info() {
    echo -e "${BLUE}ℹ${NC} $1"
}

print_success() {
    echo -e "${GREEN}✓${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}⚠${NC} $1"
}

print_error() {
    echo -e "${RED}✗${NC} $1"
}

print_header() {
    echo ""
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${BLUE}  $1${NC}"
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
}

# Standard NNS canister IDs for local development
GOVERNANCE_CANISTER="rrkah-fqaaa-aaaaa-aaaaq-cai"

# Get script directory (should be in local_sns/scripts/)
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
LOCAL_SNS_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Change to local_sns root directory
cd "$LOCAL_SNS_ROOT"

# Check if dfx is running
if ! dfx ping >/dev/null 2>&1; then
    print_error "dfx is not running. Start it with: dfx start --clean --system-canisters"
    exit 1
fi

# Deployment data check is handled by Rust code (it can work without deployment data)
# if no neuron ID is provided and no deployment data exists, Rust will prompt

print_header "Get ICP Neuron Info"

# Build command arguments - pass through whatever was provided
CMD_ARGS=("get-icp-neuron")
# Pass all provided arguments through - Rust code will handle interactive prompts for missing ones
for arg in "$@"; do
    CMD_ARGS+=("$arg")
done

cargo run --bin local_sns -- "${CMD_ARGS[@]}"
